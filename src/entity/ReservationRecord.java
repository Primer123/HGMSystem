package entity;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="reservation_records")
public class ReservationRecord {
	private int id;
	private String rsrvnum;
	private String roomnum;
	private String name;
	private int idtype;
	private String idnum;
	private String telnum;
	private String arrivingdt;
	private String departuredt;
	private String staffid;
	private String memberid;
	private int rsrvstate;
	private String remarks;
	
	@Id
	@GeneratedValue(strategy = GenerationType.AUTO)
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRsrvnum() {
		return rsrvnum;
	}
	public void setRsrvnum(String rsrvnum) {
		this.rsrvnum = rsrvnum;
	}
	public String getRoomnum() {
		return roomnum;
	}
	public void setRoomnum(String roomnum) {
		this.roomnum = roomnum;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getIdtype() {
		return idtype;
	}
	public void setIdtype(int idtype) {
		this.idtype = idtype;
	}
	public String getIdnum() {
		return idnum;
	}
	public void setIdnum(String idnum) {
		this.idnum = idnum;
	}
	public String getTelnum() {
		return telnum;
	}
	public void setTelnum(String telnum) {
		this.telnum = telnum;
	}
	public String getArrivingdt() {
		return arrivingdt;
	}
	public void setArrivingdt(String arrivingdt) {
		this.arrivingdt = arrivingdt;
	}
	public String getDeparturedt() {
		return departuredt;
	}
	public void setDeparturedt(String departuredt) {
		this.departuredt = departuredt;
	}
	public String getStaffid() {
		return staffid;
	}
	public void setStaffid(String staffid) {
		this.staffid = staffid;
	}
	public String getMemberid() {
		return memberid;
	}
	public void setMemberid(String memberid) {
		this.memberid = memberid;
	}
	public int getRsrvstate() {
		return rsrvstate;
	}
	public void setRsrvstate(int rsrvstate) {
		this.rsrvstate = rsrvstate;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
}
