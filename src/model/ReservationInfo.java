package model;

public class ReservationInfo {
	private int id;
	private String rsrvnum;
	private String roomnum;
	private String typename;
	private double stdprice;
	private double discprice;
	private String name;
	private int idtype;
	private String idnum;
	private String telnum;
	private String arrivingdt;
	private String departuredt;
	private int numOfGuest;
	private String staffname;
	private String memberid;
	private double mprice;
	private int rsrvstate;
	private String remarks;
	
	public String getRsrvnum() {
		return rsrvnum;
	}
	public void setRsrvnum(String rsrvnum) {
		this.rsrvnum = rsrvnum;
	}
	public String getRoomnum() {
		return roomnum;
	}
	public void setRoomnum(String roomnum) {
		this.roomnum = roomnum;
	}
	public String getTypename() {
		return typename;
	}
	public void setTypename(String typename) {
		this.typename = typename;
	}
	public double getStdprice() {
		return stdprice;
	}
	public void setStdprice(double stdprice) {
		this.stdprice = stdprice;
	}
	public double getDiscprice() {
		return discprice;
	}
	public void setDiscprice(double discprice) {
		this.discprice = discprice;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getIdtype() {
		return idtype;
	}
	public void setIdtype(int idtype) {
		this.idtype = idtype;
	}
	public String getIdnum() {
		return idnum;
	}
	public void setIdnum(String idnum) {
		this.idnum = idnum;
	}
	public String getTelnum() {
		return telnum;
	}
	public void setTelnum(String telnum) {
		this.telnum = telnum;
	}
	public String getArrivingdt() {
		return arrivingdt;
	}
	public void setArrivingdt(String arrivingdt) {
		this.arrivingdt = arrivingdt;
	}
	public String getDeparturedt() {
		return departuredt;
	}
	public void setDeparturedt(String departuredt) {
		this.departuredt = departuredt;
	}
	public int getNumOfGuest() {
		return numOfGuest;
	}
	public void setNumOfGuest(int numOfGuest) {
		this.numOfGuest = numOfGuest;
	}
	public String getStaffname() {
		return staffname;
	}
	public void setStaffname(String staffname) {
		this.staffname = staffname;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMemberid() {
		return memberid;
	}
	public void setMemberid(String memberid) {
		this.memberid = memberid;
	}
	public int getRsrvstate() {
		return rsrvstate;
	}
	public void setRsrvstate(int rsrvstate) {
		this.rsrvstate = rsrvstate;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public double getMprice() {
		return mprice;
	}
	public void setMprice(double mprice) {
		this.mprice = mprice;
	}
	
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ReservationInfo other = (ReservationInfo) obj;
		if (roomnum == null) {
			if (other.roomnum != null)
				return false;
		} else if (!roomnum.equals(other.roomnum))
			return false;
		if (rsrvnum == null) {
			if (other.rsrvnum != null)
				return false;
		} else if (!rsrvnum.equals(other.rsrvnum))
			return false;
		return true;
	}
	
}
